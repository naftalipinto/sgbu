    void exes() {
        try {
            List<Exemplar> list;
            list = exes.readAll();
            for (Exemplar e : list) {
                cb_exe.addItem(obra.read(e.getObraId()).getTitulo());
            }
        } catch (Exception ex) {
        }
    }

    void users() {
        try {
            List<Utilizador> list;
            list = user.readAll();
            for (Utilizador u : list) {
                cb_user.addItem(u.getNomeCompleto());
            }
        } catch (Exception ex) {
        }
    }

