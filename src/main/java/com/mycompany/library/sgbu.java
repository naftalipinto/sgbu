    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            UtilizadorService u = new UtilizadorService();
            Utilizador user = new Utilizador();
            String tipo = type.getSelectedItem().toString();
            user = u.autenticar(email.getText(), pswd.getText(), tipo);

            if (user == null) {
                JOptionPane.showMessageDialog(null, "Credenciais nÃ£o corretas!!!");
                return;
            }

            switch (tipo) {
                case "ADMIN" -> {
                    this.dispose();
                    new layout(user).setVisible(true);
                }

                case "BIBLIO" -> {
                    this.dispose();
                    new layout(user).setVisible(true);
                }

                default -> {
                    this.dispose();
                    new layout(user).setVisible(true);
                }
            }
        } catch (Exception ex) {
            System.getLogger(sgbu.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

